    public static function lookup(?string $mac): ?string
    {
        if ($mac === null || $mac === '') {
            return null;
        }

        $prefix = self::ouiPrefix($mac);

        if (isset(self::VENDORS[$prefix])) {
            return self::VENDORS[$prefix];
        }

        $secondDigit = $prefix[1] ?? '';

        if (in_array($secondDigit, ['2', '6', 'a', 'e'], true)) {
            return 'Private / randomized MAC';
        }

        return null;
    }

    private static function ouiPrefix(string $mac): string
    {
        $parts = array_map(
            fn ($part) => str_pad(strtolower(str_replace('-', ':', $part)), 2, '0', STR_PAD_LEFT),
            explode(':', strtolower(str_replace('-', ':', $mac)))
        );

        return implode(':', array_slice($parts, 0, 3));
    }
